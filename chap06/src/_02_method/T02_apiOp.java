package _02_method;

public class T02_apiOp {
	int add(int a, int b) {
		int result=a+b;
		return result;
	}
	
	int sub(int a, int b) {
		return a-b;
	}
	
	int mul(int a, int b) {
		return a*b;
	}
	
	double div (int a, int b) {
		return (double)a/b;
	}
	
	int remainder(int a, int b) {
		return a%b;
	}
}
