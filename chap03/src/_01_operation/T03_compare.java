package _01_operation;

public class T03_compare {

	public static void main(String[] args) {
		int n1 = 10;
		int n2 = 50;
		
		boolean result = n1 == n2;
		System.out.println("n1과 n2는 같? "+result);
		System.out.println("n1과 n2는 같? "+(n1 == n2));
		System.out.println("n1과 n2는 다? "+(n1 != n2));
	}

}
