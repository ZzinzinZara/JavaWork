package _01_operation;

public class T01_operation {

	public static void main(String[] args) {
		// 대입연산자
		int n1 = 10;
		n1 += 10; // n1 = n1 + 10
		n1 -= 5; // n1 = n1 - 5
		n1 *= 2; // n1 = n1 * 2
		n1 /= 3; // n1 = n1 / 3;
		n1 %= 3; // n1 = n1 % 3;
		System.out.println(n1);
		
		int n2 = 30_000_000; // _ == ,  사람이 읽기 쉽게 
		System.out.println(n2);
	}
}
